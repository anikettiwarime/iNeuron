# 7. Write a python program to Print all items by referring to their index number
# thislist = ["Java", "SQL", "C", "Reactnative", "Javascript", "Python"]

thislist = ["Java", "SQL", "C", "Reactnative", "Javascript", "Python"]

i = 0

while i < len(thislist):
    print(thislist[i],end=' ')
    i += 1
